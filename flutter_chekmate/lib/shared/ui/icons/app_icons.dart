  /// Get navigation icon based on route name
  static String getNavigationIcon(String route, {bool filled = false}) {
    switch (route) {
      case '/home':
      case '/feed':
        return filled ? homeFilled : home;
      case '/explore':
        return filled ? exploreFilled : explore;
      case '/messages':
        return filled ? messagesFilled : messages;
      case '/profile':
        return filled ? profileFilled : profile;
      case '/notifications':
        return filled ? notificationsFilled : notifications;
      default:
        return home;
    }
  }

  /// Get action icon based on action type
  static String getActionIcon(String action, {bool active = false}) {
    switch (action) {
      case 'like':
        return active ? likeFilled : like;
      case 'bookmark':
      case 'save':
        return active ? bookmarkFilled : bookmark;
      case 'star':
      case 'favorite':
        return active ? starFilled : star;
      case 'comment':
        return comment;
      case 'share':
        return share;
      case 'send':
        return send;
      default:
        return more;
    }
  }

